function handleClick(idx) {
    console.log(idx);
    const curr = korokTable.children[idx];
    if (curr.style.backgroundColor !== "grey") {
        curr.style.backgroundColor = "grey";
        completed[idx] = true;
    } else {
        curr.style.backgroundColor = "black";
        completed[idx] = false;
    }
}

